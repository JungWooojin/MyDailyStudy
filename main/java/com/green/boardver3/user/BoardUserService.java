@Service
public class BoardUserService {
    private final BoardUserMapper mapper;
    private final CommonUtils commonUtils;

     @Value("${file.dir}")
    private String fileDir = "D:/download/board3";

   @Autowired
    public BoardUserService(BoardUserMapper mapper, CommonUtils commonUtils){
       this.mapper= mapper;
       this.commonUtils = commonUtils;
   }


    public int insUser(BoardUserInsDto dto){
        //성별 항상 대문자 변경
        char gender = Character.toUpperCase(dto.getGender());
        if (!(gender == 'M' || gender == 'F')) {
            return -1;
        }
        dto.setGender(gender);

        //비밀번호 암호화
//        String upw = dto.getUpw();
//        String changeUpw = commonUtils.encodeSha256(upw);
//        dto.setUpw(changeUpw);

        dto.setUpw(commonUtils.encodeSha256(dto.getUpw()));
        try {
            return mapper.insUser(dto);
        } catch (Exception e) {
            e.printStackTrace();
            return 0;
        }
    }

    public int login(BoardUserLoginDto dto){
        UserLoginVo vo = mapper.selUserByUid(dto);//객체화해줬으니까 null
         if(vo==null){
             return 2;
         }else if(vo.getUpw().equals(commonUtils.encodeSha256(dto.getUpw()))){
             return 1;
        }   return 3;
    }

    public int updUser(BoardUserUpdDto dto){
        dto.setUpw(commonUtils.encodeSha256(dto.getUpw()));
        return mapper.updUser(dto);
    }

    public int updUserPic(MultipartFile pic, UserPatchPicDto dto){
       // user/pk/uuid.jpg
       // user/1/abcd.jpg
        String centerPath = String.format("user/%d", dto.getIuser());//2
        String dicPath = String.format("%s/%s", fileDir, centerPath);//1+2

        File dic = new File(dicPath);
        if(!dic.exists()) { //!이므로 true가 되서
            dic.mkdirs(); // 폴더를 만든다
        }

        String originFileName = pic.getOriginalFilename();
        String savedFileName = FIleUtils.makeRandomFileNm(originFileName);
        String savedFilePath = String.format("%s/%s", centerPath, savedFileName);
        String targetPath = String.format("%s/%s", fileDir, savedFilePath);
        File target = new File(targetPath);
        try {
            pic.transferTo(target);
        }catch (Exception e) {
            return 0;
        }
        dto.setMainPic(savedFilePath);
        try {
            int result = mapper.updUserPic(dto);//업데이트 시도중에 파일 오류 뜨면 파일 삭제해주는 문구
            if(result == 0) {// db에 값이 없을 때 --> iuser가 없음 xml파일을 참고해야함
                throw new Exception("프로필 사진을 등록할 수 없습니다.");
            }
        } catch (Exception e) {
            //파일 삭제
            target.delete(); //upd오류시 파일삭제
            return 0;
        }
        return 1;
    }
}
